#[cfg(test)]
mod tests {
use super::super::modules::*;
#[test]
fn test_Mux4Way16(){
let mut m = Mux4Way16::new();
m.a = 0;
m.b = 0;
m.c = 0;
m.d = 0;
m.sel = 0;
m.prop();
assert_eq!(m.sel, 0);
assert_eq!(m.b, 0);
assert_eq!(m.c, 0);
assert_eq!(m.out, 0);
assert_eq!(m.d, 0);
assert_eq!(m.a, 0);
m.sel = 1;
m.prop();
assert_eq!(m.sel, 1);
assert_eq!(m.b, 0);
assert_eq!(m.c, 0);
assert_eq!(m.out, 0);
assert_eq!(m.d, 0);
assert_eq!(m.a, 0);
m.sel = 2;
m.prop();
assert_eq!(m.sel, 2);
assert_eq!(m.b, 0);
assert_eq!(m.c, 0);
assert_eq!(m.out, 0);
assert_eq!(m.d, 0);
assert_eq!(m.a, 0);
m.sel = 3;
m.prop();
assert_eq!(m.sel, 3);
assert_eq!(m.b, 0);
assert_eq!(m.c, 0);
assert_eq!(m.out, 0);
assert_eq!(m.d, 0);
assert_eq!(m.a, 0);
m.a = 4660;
m.b = 39030;
m.c = 43690;
m.d = 21845;
m.sel = 0;
m.prop();
assert_eq!(m.sel, 0);
assert_eq!(m.b, 39030);
assert_eq!(m.c, 43690);
assert_eq!(m.out, 4660);
assert_eq!(m.d, 21845);
assert_eq!(m.a, 4660);
m.sel = 1;
m.prop();
assert_eq!(m.sel, 1);
assert_eq!(m.b, 39030);
assert_eq!(m.c, 43690);
assert_eq!(m.out, 39030);
assert_eq!(m.d, 21845);
assert_eq!(m.a, 4660);
m.sel = 2;
m.prop();
assert_eq!(m.sel, 2);
assert_eq!(m.b, 39030);
assert_eq!(m.c, 43690);
assert_eq!(m.out, 43690);
assert_eq!(m.d, 21845);
assert_eq!(m.a, 4660);
m.sel = 3;
m.prop();
assert_eq!(m.sel, 3);
assert_eq!(m.b, 39030);
assert_eq!(m.c, 43690);
assert_eq!(m.out, 21845);
assert_eq!(m.d, 21845);
assert_eq!(m.a, 4660);
}
}
